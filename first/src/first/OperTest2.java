package first;

public class OperTest2 {

	public static void main(String[] args) {

		int i = -10;
		i = +i;
		System.out.println(i);
		i = -i;
		System.out.println(i);
		
		
		boolean power = false;
		System.out.println(power);
		power = !power;	// 논리연산자를 반대로 뒤집음
		System.out.println(power);
		
		int j =10;
		System.out.println(j);
		j = ~j;
		System.out.println(j);
		
		
		
	}

}
